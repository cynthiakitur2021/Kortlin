fun main(args: Array<String>) {

    println("Enter The Numbers To be added")
    var num1 = readLine()!!.toInt()
    var num2 = readLine()!!.toInt()

    var addition = num1 + num2

    println("The sum is $addition")

    println("Enter The Numbers To be subtracted")
    var a = readLine()!!.toInt()
    var b = readLine()!!.toInt()

    var subtraction = a - b

    println("The sum is $subtraction")

    println("Enter The Numbers To be multiplied")
    var c = readLine()!!.toInt()
    var d = readLine()!!.toInt()

    var multiplication = c * d

    println("The sum is $multiplication")

    println("Enter The Numbers To be divided")
    var e = readLine()!!.toInt()
    var f = readLine()!!.toInt()

    var division = e / f

    println("The sum is $division")


}